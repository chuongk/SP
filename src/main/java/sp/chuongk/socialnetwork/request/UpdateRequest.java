package sp.chuongk.socialnetwork.request;

public class UpdateRequest {
	String sender;
	String text;
	
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}	
}
